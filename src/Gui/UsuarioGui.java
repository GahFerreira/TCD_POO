    private void BPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPesquisarActionPerformed
        // TODO add your handling code here:
          Usuario usuarios= new Usuario();
        usuarios.setPesquisa(TextPesquisar.getText());
        
        UsuarioDao dao = null;
             try {
                 dao = new UsuarioDao();
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(UsuarioGui.class.getName()).log(Level.SEVERE, null, ex);
             }
        try {
            dao.buscaUsuario(usuarios);
            TextId2.setText(String.valueOf(usuarios.getId()));
            TextNome.setText(usuarios.getNome());
            TextCpf.setText(usuarios.getCpf());
            TextSexo.setText(usuarios.getSexo());
            TextTelefone.setText(usuarios.getTelefone());
            TextGerente.setText(String.valueOf(usuarios.getGerente()));
            TextUsuario.setText(usuarios.getUser());
            TextSenha.setText(usuarios.getSenha());
        } catch (SQLException ex) {
            Logger.getLogger(UsuarioGui.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BPesquisarActionPerformed

