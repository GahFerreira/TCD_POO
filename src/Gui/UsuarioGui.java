    private void BCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BCadastrarActionPerformed
        // TODO add your handling code here:

        Usuario usuarios= new Usuario();
        usuarios.setNome(TextNome.getText());
        usuarios.setCpf(TextCpf.getText());
        usuarios.setSexo(TextSexo.getText());
        usuarios.setTelefone(TextTelefone.getText());
        usuarios.setGerente(Boolean.parseBoolean( TextGerente.getText()));
        usuarios.setUser(TextUsuario.getText());
        usuarios.setSenha(TextSenha.getText());

        if ((TextNome.getText().isEmpty()) || (TextUsuario.getText().isEmpty()) ) {
            JOptionPane.showMessageDialog(null, "Os campos n√£o podem retornar vazios");
        }
        else{
            UsuarioDao dao = null;
            try {
                dao = new UsuarioDao();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ClienteGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            dao.adiciona(usuarios);
            JOptionPane.showMessageDialog(null, "Usuario "+TextNome.getText()+" inserido com sucesso! ");

        }

        TextNome.setText("");
        TextCpf.setText("");
        TextSexo.setText("");
        TextTelefone.setText("");
        TextGerente.setText("");
        TextUsuario.setText("");
        TextSenha.setText("");

    }//GEN-LAST:event_BCadastrarActionPerformed

