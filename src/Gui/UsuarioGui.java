    private void BExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BExcluirActionPerformed
        // TODO add your handling code here:
          Usuario usuarios=new Usuario();
        int resposta=0;
        resposta=JOptionPane.showConfirmDialog(this, "Deseja realmete excluir?");
        if(resposta==JOptionPane.YES_OPTION){
            usuarios.setId(Integer.parseInt(TextId2.getText()));
            try {
                UsuarioDao dao= new UsuarioDao();
                dao.excluir(usuarios);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(UsuarioGui.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(UsuarioGui.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
         TextNome.setText("");
        TextCpf.setText("");
        TextSexo.setText("");
        TextTelefone.setText("");
        TextGerente.setText("");
        TextUsuario.setText("");
        TextSenha.setText("");
    }//GEN-LAST:event_BExcluirActionPerformed

