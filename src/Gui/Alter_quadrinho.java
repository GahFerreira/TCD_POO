    private void BAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAlterarActionPerformed
        // TODO add your handling code here:
        
         Quadrinho quadrinhos= new Quadrinho();
        
        quadrinhos.setId(Integer.parseInt(TextId.getText()));
        quadrinhos.setIsbn(TextIsbn.getText());
        quadrinhos.setTitulo(TextTitulo.getText());
        quadrinhos.setAutor(TextAutor.getText());
        quadrinhos.setEditora(TextEditora.getText());
        quadrinhos.setCategoria(TextCategoria.getText());
        quadrinhos.setVolume(Integer.parseInt(TextVolume.getText()));
       
        
        if ((TextTitulo.getText().isEmpty()) ) {
            JOptionPane.showMessageDialog(null, "Os campos n√£o podem retornar vazios");
        }
        
        else{
            QuadrinhoDao dao = null;
            try {
                dao = new QuadrinhoDao();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Alter_quadrinho.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                dao.altera(quadrinhos);
            } catch (SQLException ex) {
                Logger.getLogger(Alter_cliente.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Quadrinho "+TextTitulo.getText()+" Alterado com sucesso! ");

        }
            TextId.setText("");
            TextIsbn.setText("");
            TextTitulo.setText("");
            TextAutor.setText("");
            TextEditora.setText("");
            TextCategoria.setText("");
            TextVolume.setText("");
    }//GEN-LAST:event_BAlterarActionPerformed

    private void BPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPesquisarActionPerformed
        // TODO add your handling code here:
        
           Quadrinho quadrinhos= new Quadrinho();
           quadrinhos.setPesquisa(TextPesquisar.getText());
        
          QuadrinhoDao dao = null;
             try {
                 dao = new QuadrinhoDao();
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(Alter_quadrinho.class.getName()).log(Level.SEVERE, null, ex);
             }
        try {
            dao.buscaQuadrinho(quadrinhos);
            TextId.setText(String.valueOf(quadrinhos.getId()));
            TextIsbn.setText(quadrinhos.getIsbn());
            TextTitulo.setText(quadrinhos.getTitulo());
            TextAutor.setText(quadrinhos.getAutor());
            TextEditora.setText(quadrinhos.getEditora());
            TextCategoria.setText(quadrinhos.getCategoria());
            TextVolume.setText(String.valueOf(quadrinhos.getVolume()));
        } catch (SQLException ex) {
            Logger.getLogger(Alter_quadrinho.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_BPesquisarActionPerformed

