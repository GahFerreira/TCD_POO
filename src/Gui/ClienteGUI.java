    private void BPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPesquisarActionPerformed
        // TODO add your handling code here:
        Cliente clientes= new Cliente();
        clientes.setPesquisa(TextPesquisa.getText());
        
        ClienteDao dao = null;
             try {
                 dao = new ClienteDao();
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(ClienteGUI.class.getName()).log(Level.SEVERE, null, ex);
             }
        try {
            dao.buscaCliente(clientes);
            TextId1.setText(String.valueOf(clientes.getId()));
            TextNome.setText(clientes.getNome());
            TextCpf.setText(clientes.getCpf());
            TextEndereco.setText(clientes.getEndereco());
            TextSexo.setText(clientes.getSexo());
            TextTelefone.setText(clientes.getTelefone());
        } catch (SQLException ex) {
            Logger.getLogger(ClienteGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_BPesquisarActionPerformed

    private void BExluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BExluirActionPerformed
        // TODO add your handling code here:
        Cliente clientes=new Cliente();
        int resposta=0;
        resposta=JOptionPane.showConfirmDialog(this, "Deseja realmete excluir?");
        if(resposta==JOptionPane.YES_OPTION){
            clientes.setId(Integer.parseInt(TextId1.getText()));
            try {
                ClienteDao dao= new ClienteDao();
                dao.excluir(clientes);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ClienteGUI.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(ClienteGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
            TextId1.setText("");
            TextNome.setText("");
            TextCpf.setText("");
            TextEndereco.setText("");
            TextSexo.setText("");
            TextTelefone.setText("");
    }//GEN-LAST:event_BExluirActionPerformed

