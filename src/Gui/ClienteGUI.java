    private void BCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BCadastrarActionPerformed
        // Açoes do botao Cadastrar
         Cliente clientes= new Cliente();
         clientes.setNome(TextNome.getText());
         clientes.setCpf(TextCpf.getText());
         clientes.setEndereco(TextEndereco.getText());
         clientes.setSexo(TextSexo.getText());
         clientes.setTelefone(TextTelefone.getText());
         
         if ((TextNome.getText().isEmpty()) || (TextTelefone.getText().isEmpty()) ) {
           JOptionPane.showMessageDialog(null, "Os campos não podem retornar vazios");
         }
         else{
            ClienteDao dao = null;
             try {
                 dao = new ClienteDao();
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(ClienteGUI.class.getName()).log(Level.SEVERE, null, ex);
             }
        dao.adiciona(clientes);
        JOptionPane.showMessageDialog(null, "Cliente "+TextNome.getText()+" inserido com sucesso! ");

         }
         
            TextNome.setText("");
            TextCpf.setText("");
            TextEndereco.setText("");
            TextSexo.setText("");
            TextTelefone.setText("");
        
        
        
    }//GEN-LAST:event_BCadastrarActionPerformed

