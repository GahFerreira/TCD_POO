    private void BPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPesquisarActionPerformed
        // TODO add your handling code here:
         Emprestimo emprestimos= new Emprestimo();
        emprestimos.setPesquisa(TextPesquisar.getText());
        
        EmprestimoDao dao = null;
             try {
                 dao = new EmprestimoDao();
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(Alter_emprestimo.class.getName()).log(Level.SEVERE, null, ex);
             }
        try {
            dao.buscaEmprestimo(emprestimos);
            TextId.setText(String.valueOf(emprestimos.getId()));
            TextDtEmprestimo.setText(emprestimos.getDtEmprestimo());
            TextDtDevolucao.setText(emprestimos.getDtDevolucao());
            TextMulta.setText(String.valueOf(emprestimos.getMulta()));
            TextStatus.setText(String.valueOf(emprestimos.getStatus()));
        } catch (SQLException ex) {
            Logger.getLogger(Alter_emprestimo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_BPesquisarActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
           Emprestimo emprestimos= new Emprestimo();
        
        emprestimos.setId(Integer.parseInt(TextId.getText()));
        emprestimos.setDtEmprestimo(TextDtEmprestimo.getText());
        emprestimos.setDtEmprestimo(TextDtDevolucao.getText());
        emprestimos.setMulta(Double.parseDouble(TextMulta.getText()));
        emprestimos.setStatus(Boolean.parseBoolean(TextStatus.getText()));
       
        
        if ((TextMulta.getText().isEmpty()) ) {
            JOptionPane.showMessageDialog(null, "Os campos n√£o podem retornar vazios");
        }
        
        else{
            EmprestimoDao dao = null;
            try {
                dao = new EmprestimoDao();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Alter_emprestimo.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                dao.altera(emprestimos);
            } catch (SQLException ex) {
                Logger.getLogger(Alter_emprestimo.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Emprestimo "+TextId.getText()+" Alterado com sucesso! ");

        }
            TextId.setText("");
            TextDtEmprestimo.setText("");
            TextDtDevolucao.setText("");
            TextMulta.setText("");
            TextStatus.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

