    private void BPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPesquisarActionPerformed
        // TODO add your handling code here:
         Venda vendas= new Venda();
        vendas.setPesquisa(TextPesquisar.getText());
        
        VendaDao dao = null;
             try {
                 dao = new VendaDao();
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(Alter_venda.class.getName()).log(Level.SEVERE, null, ex);
             }
        try {
            dao.buscaVenda(vendas);
            TextId.setText(String.valueOf(vendas.getId()));
            TextDtCompra.setText(vendas.getDtCompra());
            TextValor.setText(String.valueOf(vendas.getValor()));
        } catch (SQLException ex) {
            Logger.getLogger(Alter_venda.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         
        
    }//GEN-LAST:event_BPesquisarActionPerformed

