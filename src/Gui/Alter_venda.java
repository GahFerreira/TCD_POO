    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
           Venda vendas= new Venda();
        
        vendas.setId(Integer.parseInt(TextId.getText()));
        vendas.setDtCompra(TextDtCompra.getText());
        vendas.setValor(Double.parseDouble(TextValor.getText()));
       
        
        if ((TextValor.getText().isEmpty()) ) {
            JOptionPane.showMessageDialog(null, "Os campos n√£o podem retornar vazios");
        }
        
        else{
            VendaDao dao = null;
            try {
                dao = new VendaDao();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Alter_venda.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                dao.altera(vendas);
            } catch (SQLException ex) {
                Logger.getLogger(Alter_venda.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Venda "+TextId.getText()+" Alterado com sucesso! ");

        }
        
        
        
         
            TextId.setText("");
            TextDtCompra.setText("");
            TextValor.setText("");
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

