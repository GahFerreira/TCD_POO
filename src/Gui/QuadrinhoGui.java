    private void BPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPesquisarActionPerformed
        // TODO add your handling code here:
         Quadrinho quadrinhos= new Quadrinho();
           quadrinhos.setPesquisa(TextPesquisar.getText());
        
          QuadrinhoDao dao = null;
             try {
                 dao = new QuadrinhoDao();
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(Alter_quadrinho.class.getName()).log(Level.SEVERE, null, ex);
             }
        try {
            dao.buscaQuadrinho(quadrinhos);
            TextId.setText(String.valueOf(quadrinhos.getId()));
            TextIsbn.setText(quadrinhos.getIsbn());
            TextTitulo.setText(quadrinhos.getTitulo());
            TextAutor.setText(quadrinhos.getAutor());
            TextEditora.setText(quadrinhos.getEditora());
            TextCategoria.setText(quadrinhos.getCategoria());
            TextVolume.setText(String.valueOf(quadrinhos.getVolume()));
        } catch (SQLException ex) {
            Logger.getLogger(Alter_quadrinho.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BPesquisarActionPerformed

    private void BExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BExcluirActionPerformed
        // TODO add your handling code here:
        Quadrinho quadrinhos=new Quadrinho();
        int resposta=0;
        resposta=JOptionPane.showConfirmDialog(this, "Deseja realmete excluir?");
        if(resposta==JOptionPane.YES_OPTION){
            quadrinhos.setId(Integer.parseInt(TextId.getText()));
            try {
                QuadrinhoDao dao= new QuadrinhoDao();
                dao.excluir(quadrinhos);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(QuadrinhoGui.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(QuadrinhoGui.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
            TextId.setText("");
            TextIsbn.setText("");
            TextTitulo.setText("");
            TextAutor.setText("");
            TextEditora.setText("");
            TextCategoria.setText("");
            TextVolume.setText("");
        
    }//GEN-LAST:event_BExcluirActionPerformed

