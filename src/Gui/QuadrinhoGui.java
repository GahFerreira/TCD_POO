    private void BCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BCadastrarActionPerformed
        // TODO add your handling code here:
        
        Quadrinho quadrinhos= new Quadrinho();
         quadrinhos.setIsbn(TextIsbn.getText());
         quadrinhos.setTitulo(TextTitulo.getText());
         quadrinhos.setAutor(TextAutor.getText());
         quadrinhos.setEditora(TextEditora.getText());
         quadrinhos.setCategoria(TextCategoria.getText());
         quadrinhos.setVolume(Integer.parseInt(TextVolume.getText()));
         
         if ((TextIsbn.getText().isEmpty()) || (TextTitulo.getText().isEmpty()) ) {
           JOptionPane.showMessageDialog(null, "Os campos n√£o podem retornar vazios");
         }
         else{
            QuadrinhoDao dao = null;
             try {
                 dao = new QuadrinhoDao();
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(ClienteGUI.class.getName()).log(Level.SEVERE, null, ex);
             }
        dao.adiciona(quadrinhos);
        JOptionPane.showMessageDialog(null, "Quadrinho "+TextTitulo.getText()+" inserido com sucesso! ");

         }
            TextId.setText("");
            TextIsbn.setText("");
            TextTitulo.setText("");
            TextAutor.setText("");
            TextEditora.setText("");
            TextCategoria.setText("");
            TextVolume.setText("");
    }//GEN-LAST:event_BCadastrarActionPerformed

