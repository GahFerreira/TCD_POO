    private void BAlterar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAlterar1ActionPerformed
        // TODO add your handling code here:
         
        Cliente clientes= new Cliente();
        
        clientes.setId(Integer.parseInt(TextId.getText()));
        clientes.setNome(TextNome1.getText());
        clientes.setCpf(TextCpf1.getText());
        clientes.setEndereco(TextEndereco1.getText());
        clientes.setSexo(TextSexo1.getText());
        clientes.setTelefone(TextTelefone1.getText());
       
        
        if ((TextTelefone1.getText().isEmpty()) ) {
            JOptionPane.showMessageDialog(null, "Os campos n√£o podem retornar vazios");
        }
        
        else{
            ClienteDao dao = null;
            try {
                dao = new ClienteDao();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ClienteGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                dao.altera(clientes);
            } catch (SQLException ex) {
                Logger.getLogger(Alter_cliente.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Cliente "+TextNome1.getText()+" Alterado com sucesso! ");

        }
            TextId.setText("");
            TextNome1.setText("");
            TextCpf1.setText("");
            TextEndereco1.setText("");
            TextSexo1.setText("");
            TextTelefone1.setText("");
        
    }//GEN-LAST:event_BAlterar1ActionPerformed

    private void BPesquisar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPesquisar1ActionPerformed
        // TODO add your handling code here:
        Cliente clientes= new Cliente();
        clientes.setPesquisa(TextPesquisa.getText());

        ClienteDao dao = null;
        try {
            dao = new ClienteDao();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Alter_cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            dao.buscaCliente(clientes);
            TextId.setText(String.valueOf(clientes.getId()));
            TextNome1.setText(clientes.getNome());
            TextCpf1.setText(clientes.getCpf());
            TextEndereco1.setText(clientes.getEndereco());
            TextSexo1.setText(clientes.getSexo());
            TextTelefone1.setText(clientes.getTelefone());
        } catch (SQLException ex) {
            Logger.getLogger(Alter_cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BPesquisar1ActionPerformed

