    private void BPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPesquisarActionPerformed
        // TODO add your handling code here:
        Emprestimo emprestimos= new Emprestimo();
        emprestimos.setPesquisa(TextPesquisar.getText());
        
        EmprestimoDao dao = null;
             try {
                 dao = new EmprestimoDao();
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(EmprestimoGui.class.getName()).log(Level.SEVERE, null, ex);
             }
        try {
            dao.buscaEmprestimo(emprestimos);
            TextId.setText(String.valueOf(emprestimos.getId()));
            TextDtEmprestimo.setText(emprestimos.getDtEmprestimo());
            TextDtDevolucao.setText(emprestimos.getDtDevolucao());
            TextMulta.setText(String.valueOf(emprestimos.getMulta()));
            TextStatus.setText(String.valueOf(emprestimos.getStatus()));
        } catch (SQLException ex) {
            Logger.getLogger(EmprestimoGui.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_BPesquisarActionPerformed

    private void BExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BExcluirActionPerformed
        // TODO add your handling code here:
        Emprestimo emprestimos=new Emprestimo();
        int resposta=0;
        resposta=JOptionPane.showConfirmDialog(this, "Deseja realmete excluir?");
        if(resposta==JOptionPane.YES_OPTION){
            emprestimos.setId(Integer.parseInt(TextId.getText()));
            try {
                EmprestimoDao dao= new EmprestimoDao();
                dao.excluir(emprestimos);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(EmprestimoGui.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(EmprestimoGui.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
            TextId.setText("");
            TextDtEmprestimo.setText("");
            TextDtDevolucao.setText("");
            TextMulta.setText("");
            TextStatus.setText("");
    }//GEN-LAST:event_BExcluirActionPerformed

