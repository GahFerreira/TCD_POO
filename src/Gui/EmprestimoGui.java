    private void BCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BCadastrarActionPerformed
        // TODO add your handling code here:
        
        Emprestimo emprestimos= new Emprestimo();
         
         emprestimos.setDtEmprestimo(TextDtEmprestimo.getText());
         emprestimos.setDtDevolucao(TextDtDevolucao.getText());
          emprestimos.setMulta(Double.parseDouble(TextMulta.getText()));
          emprestimos.setStatus(Boolean.parseBoolean(TextStatus.getText()));
         
            if ((TextDtEmprestimo.getText().isEmpty()) ) {
           JOptionPane.showMessageDialog(null, "Os campos n√£o podem retornar vazios");
         }
         else{
            EmprestimoDao dao = null;
             try {
                 dao = new EmprestimoDao();
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(ClienteGUI.class.getName()).log(Level.SEVERE, null, ex);
             }
        dao.adiciona(emprestimos);
        JOptionPane.showMessageDialog(null, "Venda inserida com sucesso! ");

         }
         
            TextDtEmprestimo.setText("");
            TextDtDevolucao.setText("");
            TextMulta.setText("");
            TextStatus.setText("");
    }//GEN-LAST:event_BCadastrarActionPerformed

